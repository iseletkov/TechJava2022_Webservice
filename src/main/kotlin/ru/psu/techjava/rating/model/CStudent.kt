 *******************************************************************************************************/
class CStudent
/********************************************************************************************************
 * Первичный конструктор.                                                                               *
 *******************************************************************************************************/
(
    /****************************************************************************************************
    * Идентификатор.                                                                                    *
    ****************************************************************************************************/
    var id                                  : UUID?
                                            = null,

    /****************************************************************************************************
     * Ф.И.О.                                                                                           *
     ***************************************************************************************************/
    var name                                : String?
                                            = null
)
{
    /****************************************************************************************************
     * Получение строки из объекта класса.                                                              *
     ***************************************************************************************************/
    override fun toString()                 =  "id: $id name: $name"

    /****************************************************************************************************
     * Список оценок.                                                                                   *
     ***************************************************************************************************/
    var marks                               : List<CMark>
                                            = mutableListOf()

}